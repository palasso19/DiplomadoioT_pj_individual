bool boton1LeerEstado(){
	uint32_t valor_pin;
	bool resultado;
	valor_pin=GPIO_PinRead(GPIOC,3);
	if(valor_pin!=0)
		resultado=true;
	else
		resultado=false;

	return(resultado);


}

